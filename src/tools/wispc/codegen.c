int codegen_emit(const char *dir, Unit *u, SemaResult *r) {
    SrcInst srcs[32];
    int nsrc = collect_srcs(u, srcs, 32);
    if (nsrc < 0) return 1;

    /* Collect const/mut. */
    Decl *konst[64]; int nkonst = 0;
    for (int i = 0; i < u->n; i++)
        if (u->decls[i]->kind == D_CONST || u->decls[i]->kind == D_MUT)
            if (nkonst < 64) konst[nkonst++] = u->decls[i];

    CGCtx ctx = { .srcs = srcs, .nsrc = nsrc, .konst = konst, .nkonst = nkonst, .r = r };

    char path[1024]; FILE *f;
    snprintf(path, sizeof path, "%s/gen_overrides.h", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    emit_overrides(f, u, &ctx); fclose(f);

    snprintf(path, sizeof path, "%s/gen_sources.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    emit_sources(f, srcs, nsrc); fclose(f);

    snprintf(path, sizeof path, "%s/gen_bindings.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    emit_bindings(f, srcs, nsrc, r, &ctx); fclose(f);

    snprintf(path, sizeof path, "%s/gen_surfaces.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    int rc = emit_surfaces(f, u, &ctx); fclose(f);
    if (rc) return rc;

    /* Emit spawn_<name>() shims for every spawned_by template. The args are
     * the $-names sema collected (first-seen order). For "osd" specifically
     * we route through the existing osd_post() runtime; for any other name
     * we emit a no-op stub so emit() callsites link cleanly. Real DSL-driven
     * slot allocator + render is deferred to Session 5b. */
    snprintf(path, sizeof path, "%s/gen_spawn.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    fputs("/* Generated by wispc. Do not edit. */\n", f);
    fputs("#include \"wisp.h\"\n#include <string.h>\n\n", f);

    int osd_in_spawn = 0;
    for (int i = 0; i < r->nspawned; i++)
        if (r->spawned_names[i] && strcmp(r->spawned_names[i], "osd") == 0) { osd_in_spawn = 1; break; }

    /* Session 5c: pull the OSD engine into this TU so the DSL drives whether
     * (and with which behavior flags from gen_overrides.h) the engine compiles
     * at all. src/osd.c is no longer linked directly when DSL-spawned. */
    if (osd_in_spawn) {
        fputs("/* === osd engine, instantiated via DSL spawned_by template === */\n", f);
        fputs("#ifdef WISP_HAS_OSD\n", f);
        fputs("#include \"osd.c\"\n", f);
        fputs("#endif\n\n", f);
    }

    for (int i = 0; i < r->nspawned; i++) {
        const char *snm = r->spawned_names[i];
        const char **args = r->spawned_args[i];
        (void)args;
        if (strcmp(snm, "osd") == 0) {
            fputs("#ifdef WISP_HAS_OSD\n", f);
            fputs("void spawn_osd(const char *summary, uint32_t icon, int pct, int muted) {\n"
                  "    osd_post(0, summary ? summary : \"\", \"\", icon, pct, 0, muted, OSD_TIMEOUT_OSD);\n"
                  "}\n", f);
            fputs("#else\n", f);
            fputs("void spawn_osd(const char *s, uint32_t i, int p, int m) { (void)s;(void)i;(void)p;(void)m; }\n", f);
            fputs("#endif\n\n", f);
        } else {
            fprintf(f, "/* spawn_%s: no-op stub; non-osd templates need a generic slot allocator */\n", snm);
            fprintf(f, "void spawn_%s() { }\n\n", snm);
        }
    }
    fclose(f);

    snprintf(path, sizeof path, "%s/gen_outputs.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    rc = emit_outputs(f, u, r); fclose(f);
    if (rc) return rc;

    snprintf(path, sizeof path, "%s/main.c", dir);
    f = fopen(path, "w"); if (!f) { perror(path); return 1; }
    emit_main(f, srcs, nsrc, r); fclose(f);

    return 0;
}
